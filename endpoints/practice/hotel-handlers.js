function getHotelConfigurationById(req, res) {
  try {
    const hotelId = req.params.id;
    logDebug(`Getting hotel configuration for ${hotelId}`);

    if (hotelConfigurations[hotelId]) {
      return res.status(HTTP_OK).json(hotelConfigurations[hotelId]);
    } else {
      return res.status(HTTP_NOT_FOUND).json(formatErrorResponse(`Hotel configuration not found for ${hotelId}`));
    }
  } catch (error) {
    logDebug("Error getting hotel configuration by id", error);
    return res.status(HTTP_INTERNAL_SERVER_ERROR).json(formatErrorResponse("Error retrieving hotel configuration"));
  }
}

function getHotelById(req, res) {
  try {
    const hotelId = req.params.id;
    logDebug(`Getting hotel data for ${hotelId}`);

    const hotel = hotelsData.find((h) => h.id === hotelId);
    if (hotel) {
      return res.status(HTTP_OK).json(hotel);
    } else {
      return res.status(HTTP_NOT_FOUND).json(formatErrorResponse(`Hotel not found with id ${hotelId}`));
    }
  } catch (error) {
    logDebug("Error getting hotel by id", error);
    return res.status(HTTP_INTERNAL_SERVER_ERROR).json(formatErrorResponse("Error retrieving hotel data"));
  }
}

